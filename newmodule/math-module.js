

const sum = (arg1, arg2) => {
  return arg1 + arg2
}

const multiply = (arg1, arg2) => {
  return arg1 * arg2
}


const divide = (arg1, arg2) => {
    return arg1 / arg2
}


const square = (arg) => {
    return Math.sqrt(arg)
}


module.exports = {
    sum,
    multiply,
    divide,
    square
}

