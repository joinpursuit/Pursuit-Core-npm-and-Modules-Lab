const sum = (num1, num2) => {
  return num1 + num2
}

const multiply = (num1, num2) => {
  return num1 * num2
}

const divide = (num1, num2) => {
  return num1 / num2
}

const square = (num) => {
  return num * num
}

module.exports = {
  sum: sum,
  multiply: multiply,
  divide: divide,
  square: square
}
